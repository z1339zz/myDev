package hi;

public class Test {

	public static void main(String[] args) {

		/* 산술연산자
		 * 숫자 연산 숫자 => 숫*/
		
		System.out.println(10 + 3); //더하기
		System.out.println(10 - 3); //뺴기
		System.out.println(10 * 3); //곱하기
		System.out.println(10 / 3); //나누기(몫)
		System.out.println(10 % 3); //나누기(나머지)
	
		
	}
}
								